void SYS_start(void)
{
  TCCR0  = 0x0a;        // Compare Match Output Mode
  TCNT0  = 0x00;
  OCR0   = SYS_CPU_CLK / 8 / SYS_FAST_INT_FREQ - 1 ;
  TIFR   = 0x07;        // clear all flags
  TIMSK  = 0x02;        // enable Compare Match Interrupt
  TCNT0  = 0x00;
  
  MCUCR  = 0x00;        // disable sleep modes
  
  SYS_ms_cnt = 0;
  SYS_100ms_cnt = 0;
  SYS_s_cnt  = 0;
  
  SYS_Fast_Lock = 0;
  sei();
}


