  PORTB =   0x00;     //   0000 0000
                      //   |||| ||||
                      //   |||| |||+ Bit  0:
                      //   |||| ||+- Bit  1:
                      //   |||| |+-- Bit  2:
                      //   |||| +--- Bit  3:
                      //   |||+----- Bit  4:
                      //   ||+------ Bit  5:
                      //   |+------- Bit  6:
                      //   +-------- Bit  7:
  
  // DDRB – The Port B Data Direction Register: 0 input  1 output
  DDRB =   0x00;      //   0000 0000
                      //   |||| ||||
                      //   |||| |||+ Bit  0:
                      //   |||| ||+- Bit  1:
                      //   |||| |+-- Bit  2:
                      //   |||| +--- Bit  3:
                      //   |||+----- Bit  4:
                      //   ||+------ Bit  5:
                      //   |+------- Bit  6:
                      //   +-------- Bit  7:

  // PORTC – The Port C Data Register
  PORTC =   0x00;     //   0010 0000
                      //   |||| ||||
                      //   |||| |||+ Bit  0:
                      //   |||| ||+- Bit  1:
                      //   |||| |+-- Bit  2:
                      //   |||| +--- Bit  3:
