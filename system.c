                      //   |||| ||||
                      //   |||| |||+ Bit  0:
                      //   |||| ||+- Bit  1:
                      //   |||| |+-- Bit  2:
                      //   |||| +--- Bit  3:
                      //   |||+----- Bit  4:
                      //   ||+------ Bit  5:
                      //   |+------- Bit  6:
                      //   +-------- Bit  7:

