/* FUNCTION                                                              */
/*                                                                       */
/*      Interrupt handler for Timer0 CompA                               */
/*                                                                       */
/* DESCRIPTION                                                           */
/*                                                                       */
/*                                                                       */
/* INPUT                                                                 */
/*                                                                       */
/*      name    description                                              */
/*                                                                       */
/* OUTPUT                                                                */
/*                                                                       */
/*      name    description                                              */
/*                                                                       */
/* RETURNS                                                               */
/*                                                                       */
/*      status  description                                              */
/*                                                                       */
/* HISTORY                                                               */
/*                                                                       */
/*        DATE          REMARKS                                          */
/*      mm-dd-yyyy                                                       */
/*                                                                       */
/*************************************************************************/
ISR(TIMER0_COMP_vect)
{
  if(!SYS_Fast_Lock)
  {
    SYS_Fast_Lock = 1;

    sei();
    MAIN_fast();
    cli();
    
    if(SYS_ms_cnt<=0)
    {
      sei();
      MAIN_1ms();
      
      if(SYS_100ms_cnt<=0)
      {
        MAIN_100ms();
        SYS_100ms_cnt = 100-1;
        if(SYS_s_cnt<=0)
        {
          MAIN_1s();
          SYS_s_cnt = 10-1;
        }
        else
        {
          SYS_s_cnt--;
        }
      }
      else
      {
        SYS_100ms_cnt --;
      }
      
      cli();
      
      SYS_ms_cnt = SYS_FAST_INT_FREQ / 1000 - 1;
    }
    else
    {
      SYS_ms_cnt--;
    }
    
    SYS_Fast_Lock = 0;
  }
}

